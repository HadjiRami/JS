let season = prompt("wat is your fav season");
switch(season){
    case "winter" :
        //code will be Display.
        alert("winter is so cold");
        break;

    case "summer" :
        alert("summer is hot");
        break;
    case "Spring" : 
    alert("spring is very fine");
    break;
    
    default :
    alert("it is ok");
    break;
}